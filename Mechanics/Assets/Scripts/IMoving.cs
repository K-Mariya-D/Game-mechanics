using UnityEngine;

/// <summary>
/// Интерфейс, поддерживающий движущиеся объекты
/// </summary>
public interface IMoving
{
    /// <summary>
    /// Скорость
    /// </summary>
    public float Speed { get; }
    /// <summary>
    /// Находится ли объект в состоянии движения?
    /// </summary>
    public bool IsMoving { get; }
}
